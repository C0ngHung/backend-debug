CREATE SEQUENCE TBL_ACCOUNT_SEQ START WITH 1 INCREMENT BY 1;

CREATE TABLE tbl_account (
    id             NUMBER(19) DEFAULT TBL_ACCOUNT_SEQ.NEXTVAL PRIMARY KEY,
    account_number VARCHAR2(50 CHAR) NOT NULL UNIQUE,
    owner_name     VARCHAR2(255 CHAR) NOT NULL,
    balance        NUMBER(19, 2) NOT NULL,
    status         VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    created_at     TIMESTAMP(6) DEFAULT LOCALTIMESTAMP NOT NULL,
    updated_at     TIMESTAMP(6) DEFAULT LOCALTIMESTAMP NOT NULL,

    CONSTRAINT ck_account_status CHECK (status IN ('ACTIVE', 'BLOCKED'))
);

CREATE SEQUENCE TBL_TRANSACTION_SEQ START WITH 1 INCREMENT BY 1;

CREATE TABLE tbl_transaction (
    id              NUMBER(19) DEFAULT TBL_TRANSACTION_SEQ.NEXTVAL PRIMARY KEY,
    from_account_id NUMBER(19) NOT NULL,
    to_account_id   NUMBER(19) NOT NULL,
    amount          NUMBER(19, 2) NOT NULL,
    status          VARCHAR2(20 CHAR) DEFAULT 'PENDING' NOT NULL,
    request_id      VARCHAR2(100 CHAR),
    created_at      TIMESTAMP(6) DEFAULT LOCALTIMESTAMP NOT NULL,

    CONSTRAINT fk_txn_from_account
        FOREIGN KEY (from_account_id)
            REFERENCES tbl_account (id),

    CONSTRAINT fk_txn_to_account
        FOREIGN KEY (to_account_id)
            REFERENCES tbl_account (id),

    CONSTRAINT uq_txn_request_id UNIQUE (request_id),
    CONSTRAINT ck_txn_status CHECK (status IN ('PENDING', 'SUCCESS', 'FAILED'))
);
